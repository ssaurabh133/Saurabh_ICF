package com.payout.vo;

public class PayReportVO {

	 private String sourceId;
	 private String sourceDesc;
	 private String activityCode;
	 private String lbxActivityCode;
	 private String reportType;
	 private String reportformat;
	 private String reportformatid;
	 
	 
	 
	public String getReportformat() {
		return reportformat;
	}
	public void setReportformat(String reportformat) {
		this.reportformat = reportformat;
	}
	public String getReportformatid() {
		return reportformatid;
	}
	public void setReportformatid(String reportformatid) {
		this.reportformatid = reportformatid;
	}
	public String getReportType() {
		return reportType;
	}
	public void setReportType(String reportType) {
		this.reportType = reportType;
	}
	public String getSourceId() {
		return sourceId;
	}
	public void setSourceId(String sourceId) {
		this.sourceId = sourceId;
	}
	public String getSourceDesc() {
		return sourceDesc;
	}
	public void setSourceDesc(String sourceDesc) {
		this.sourceDesc = sourceDesc;
	}
	public String getActivityCode() {
		return activityCode;
	}
	public void setActivityCode(String activityCode) {
		this.activityCode = activityCode;
	}
	public String getLbxActivityCode() {
		return lbxActivityCode;
	}
	public void setLbxActivityCode(String lbxActivityCode) {
		this.lbxActivityCode = lbxActivityCode;
	}
	 
	 
}
