package com.masters.vo;

public class ModuleMasterVo {
	private String moduleId;
	private String moduleDesc;
	private String modulePageName;
	private String moduleStatus;
	private String makerId;
	private String makerDate;
	
	public String getModuleId() {
		return moduleId;
	}
	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}
	public String getModuleDesc() {
		return moduleDesc;
	}
	public void setModuleDesc(String moduleDesc) {
		this.moduleDesc = moduleDesc;
	}
	public String getModulePageName() {
		return modulePageName;
	}
	public void setModulePageName(String modulePageName) {
		this.modulePageName = modulePageName;
	}
	public String getModuleStatus() {
		return moduleStatus;
	}
	public void setModuleStatus(String moduleStatus) {
		this.moduleStatus = moduleStatus;
	}
	public String getMakerId() {
		return makerId;
	}
	public void setMakerId(String makerId) {
		this.makerId = makerId;
	}
	public String getMakerDate() {
		return makerDate;
	}
	public void setMakerDate(String makerDate) {
		this.makerDate = makerDate;
	}

}
